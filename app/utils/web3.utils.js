const web3Util = function() {};

async function getFTMPrice() {
    const ftmPrice = (await (await fetch('https://api.binance.com/api/v3/avgPrice?symbol=FTMUSDT', { method: 'GET' })).json()).price;
    return ftmPrice;
}

web3Util.getTokenPrice = async function(token) {
    const ftmPrice = await getFTMPrice;
    if (token === web3Config.daiAddress) return 1;
    if (token === web3Config.wftmAddress) return Number(ftmPrice);
    const tokenContract = new web3.eth.Contract(erc20Abi, token);
    const tokenDecimals = await tokenContract.methods.decimals().call();
    const pair = await factoryContract.methods.getPair(token, web3Config.wftmAddress).call();
    if (pair != web3Config.zeroAddress) {
        const wftmBal = await wftmContract.methods.balanceOf(pair).call();
        const tokenBal = await tokenContract.methods.balanceOf(pair).call();
        const wftmBalance = web3.utils.fromWei(wftmBal.toString(), "ether");
        const tokenBalance = web3.utils.fromWei(tokenBal.toString(), unitsMap[tokenDecimals]);
        return tokenBalance / wftmBalance * ftmPrice;
    } else {
        const pair1 = await factoryContract.methods.getPair(token, web3Config.daiAddress).call();
        const daimBal = await daiContract.methods.balanceOf(pair1).call();
        const tokenBal = await tokenContract.methods.balanceOf(pair1).call();
        const daiBalance = web3.utils.fromWei(daimBal.toString(), "ether");
        const tokenBalance = web3.utils.fromWei(tokenBal.toString(), unitsMap[tokenDecimals]);
        return tokenBalance / daiBalance;
    }
}

